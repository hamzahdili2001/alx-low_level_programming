/**
 * add - add two numbers
 * @a: number one
 * @b: number two
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subs two numbers
 * @a: number one
 * @b: number two
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul two numbers
 * @a: number one
 * @b: number two
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div two numbers
 * @a: number one
 * @b: number two
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod two numbers
 * @a: number one
 * @b: number two
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
